def _to_ms(x):
    if pd.isna(x):
        return np.nan
    s = str(x).strip()
    try:
        if ":" in s:
            m, rest = s.split(":")
            return (int(m) * 60.0 + float(rest)) * 1000.0
        return float(s) * 1000.0
    except:
        return np.nan

